package video21;

public class Matrices {
    public static void main(String[] args) {
        int matriz [][] = new int [2][2];
      
        matriz [0][0] = 5;
        matriz [0][1] = 2;
        matriz [1][0] = 2;
        matriz [1][1] = 5;
       
        System.out.print("["+ matriz[0][0]+"]");
        System.out.println("["+ matriz[0][1]+"]");
        System.out.print("["+matriz[1][0]+"]");
        System.out.println("["+matriz[1][1]+"]");
    }
}
